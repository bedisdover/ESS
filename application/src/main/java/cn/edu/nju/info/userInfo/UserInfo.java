package cn.edu.nju.info.userInfo;

public class UserInfo {

    private String name;
    private String email;
    private int role;

    public UserInfo() {
    }

    public UserInfo(String name, String email, int role) {
        this.name = name;
        this.email = email;
        this.role = role;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }
}
